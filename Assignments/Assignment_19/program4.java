//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : printDigits
//  Description    : Prints digit of the number.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printDigits(int num)
   {
        int iDigit = 0;

        while(num > 0)                              // Business Logic
        {
            iDigit = num % 10;

            System.out.println(iDigit);

            num = num / 10;
        }
   }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program4
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program4
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.printDigits(9876);
    }
}


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : printReverse
//  Description    : Prints numbers in the reverse order starting from N.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printReverse(int n)
   {
        int iCnt = 0;

        for(iCnt = n; iCnt >= 1; iCnt--)                        // Business Logic
        {
           System.out.println(iCnt);
        }

        
   }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program2
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program2
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.printReverse(10);
    }
}


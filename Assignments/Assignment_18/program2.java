//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : printEvenNumbers
//  Description    : It prints all even numbers up to N.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printEvenNumbers(int n)
    {

        int iCnt = 0;

        for(iCnt = 2; iCnt <= n; iCnt = iCnt + 2)
        {
            System.out.println(iCnt);
        }

    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program2
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program2
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.printEvenNumbers(20);
    }
}


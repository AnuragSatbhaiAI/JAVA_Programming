//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : printOddNumbers
//  Description    : It prints all odd numbers up to N.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printOddNumbers(int n)
    {
        int iCnt = 0;

        for(iCnt = 1; iCnt <= n; iCnt = iCnt + 2)
        {
            System.out.println(iCnt);
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program3
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program3
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.printOddNumbers(20);
    }
}


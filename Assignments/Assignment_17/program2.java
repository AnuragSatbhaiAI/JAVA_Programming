//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : checkPalindrome
//  Description    : It checks given number is palindrome or not.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void checkPalindrome(int num)
    {
        int iDigit = 0;
        int number = num;
        int iRev = 0;


        while(num > 0)
        {
            iDigit = num % 10;

            iRev = iRev * 10 + iDigit;

            num = num / 10;
        }

        if(iRev == number)
        {
            System.out.println("Palindrome");
        }
        else
        {
            System.out.println("Not palindrome");
        }
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program2
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program2
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.checkPalindrome(121);
    }
}


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : sumOfDigits
//  Description    : It calculates the sum of digits of a number.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void sumOfDigits(int num)
    {
        int iDigit = 0;
        int iSum = 0;

        if(num < 0)
        {
            num = -num;
        }

        while(num > 0)
        {
            iDigit = num % 10;

            iSum = iSum + iDigit;

            num = num / 10;
        }

        System.out.println("Sum of digits = " + iSum);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program1
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program1
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.sumOfDigits(1234);
    }
}


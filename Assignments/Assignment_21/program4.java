//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : countFactors
//  Description    : count factors of the given number.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void countFactors(int num)
   {
        int iCnt = 0;
        int iCount = 0;


        for (iCnt = 1; iCnt <= (num/2); iCnt++)           // Business Logic   
        {
            if((num % iCnt) == 0)
            {
                iCount++;
            }
        }

        iCount++;                                          // For number itself

        System.out.println(iCount);
        
   }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program4
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program4
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.countFactors(20);
    }
}


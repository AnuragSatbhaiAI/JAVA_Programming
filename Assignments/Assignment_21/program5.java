//  5.Write a program to print all numbers from 1 to N that are divisible by both 2 & 3.
//
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Function Name  : printDivisibleBy2and3
//  Description    : count factors of the given number.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void printDivisibleBy2and3(int n)
   {
        int iCnt = 0;

        if(n < 0)                                           // Updater
        {
            n = -n;
        }


        for (iCnt = 1; iCnt <= n; iCnt++)                   // Business Logic
        {
            if(((iCnt % 2) == 0) && ((iCnt % 3) == 0))
            {
                System.out.println(iCnt);
            }
        }    
   }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program5
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program5
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.printDivisibleBy2and3(30);
    }
}


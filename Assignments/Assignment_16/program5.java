class Logic
{
    void countDigits(int num)
    {
        int iDigit = 0;
        int iCnt = 0;

        if(num < 0)
        {
            num = -num;
        }

        while(num > 0)
        {
            iDigit = num % 10;

            iCnt = iCnt + 1;

            num = num / 10;
        }

        System.out.println("Digits in number = " + iCnt);

    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program5
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program5
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.countDigits(7865);
    }
}


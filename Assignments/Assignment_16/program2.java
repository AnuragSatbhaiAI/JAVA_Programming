class Logic
{
    void checkEvenOdd(int num)
    {
        if((num % 2) == 0)
        {
            System.out.println("Even number");
        }
        else
        {
            System.out.println("Odd number");
        }

    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program2
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program2
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.checkEvenOdd(7);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//      Output : 
//      Odd number

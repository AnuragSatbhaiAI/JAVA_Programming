//  1.Write a program to calculate the sum of first N natural numbers.
//
//////////////////////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : Logic
//  Description    : Contains logic to perform summation of numbers from 1 to given input.
//  Input          : int
//  Output         : void
//  Author         : Anurag Gopal Satbhai
//  Date           : 03/11/25
//
//////////////////////////////////////////////////////////////////////////////////////////

class Logic
{
    void calculateSum(int n)
    {
        int iSum = 0;

        for(int iCnt = 1; iCnt <= n; iCnt++)
        {
            iSum = iSum + iCnt;
        }

        System.out.println("Summation is : " + iSum);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program1
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program1 
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.calculateSum(10);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//      Output : 
//      Summation is : 55
//
//////////////////////////////////////////////////////////////////////////////////////////


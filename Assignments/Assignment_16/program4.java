class Logic
{
    void reverseNumber(int num)
    {
        int iDigit = 0;

        if(num < 0)
        {
            num = -num;
            System.out.print("-");
        }

        while(num > 0)
        {
            iDigit = num % 10;

            System.out.print(iDigit);

            num = num / 10;
        }

    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//  Class Name     : program4
//  Description    : Entry point for the application.
//
//////////////////////////////////////////////////////////////////////////////////////////

public class program4
{
    public static void main (String args[])
    {
        Logic obj = new Logic();
        obj.reverseNumber(1234);
    }
}

